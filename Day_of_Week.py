dow = int(input("Enter the number of day:"))

if dow == 1:
    print("Sunday")
elif dow == 2:
    print("Monday")
elif dow == 3:
    print("Tuesday")
elif dow == 4:
    print("Wednesday")
elif dow == 5:
    print("Thursday")
elif dow == 6:
    print("Friday")
elif dow == 7:
    print("Saturday")
else:
    print("Invalid input! Please enter a number between 1 and 7.")
